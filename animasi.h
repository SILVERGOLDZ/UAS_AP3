void delay(int x){
    this_thread::sleep_for(chrono::milliseconds(x));
}

void char_delay(const string& teks, int ms, int x, int y) {
    if (x == 1){
        system("cls");
    }

    for (char c : teks) {
        cout << c << flush; 
        Beep(620,ms);
        delay(ms); 
    } 
    if (y == 1){
        getch();
    }
}

